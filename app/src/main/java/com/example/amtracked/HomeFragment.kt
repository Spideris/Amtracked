class HomeFragment : Fragment() {

    override fun onCreate(savedInstanceState: Bundle?) {
        super.onCreate(savedInstanceState)
    }

    override fun onCreateView(
        inflater: LayoutInflater, container: ViewGroup?,
        savedInstanceState: Bundle?
    ): View? {
        // Inflate the layout for this fragment
        return inflater.inflate(R.layout.home_fragment, container, false)
    }

    override fun onViewCreated(view: View, savedInstanceState: Bundle?) {
        super.onViewCreated(view, savedInstanceState)

        val buttonTrains: Button = view.findViewById(R.id.trains)
        buttonTrains.setOnClickListener {
            parentFragmentManager.beginTransaction().replace(R.id.fragmentContainerView, TrainListFragment.newInstance()).addToBackStack(null).commit()
        }

        val buttonStations: Button = view.findViewById(R.id.station_metas)
        buttonStations.setOnClickListener{
            parentFragmentManager.beginTransaction().replace(R.id.fragmentContainerView, StationMetaListFragment.newInstance()).addToBackStack(null).commit()
        }
    }

